/*******************LCD_CLS 清局屏x8********************/
void LCD_CLS_part8(uint8 x,uint8 y,uint8 x_size)
{
  LCD_DC_high;          //输入数据
  LCD_Pos(x,y);
  for(uint8 j=x;j<x+x_size;j++)
    LCD_write(0x00);
}

/*******************LCD_CLS 清局屏x16********************/
void LCD_CLS_part16(uint8 x,uint8 y,uint8 x_size)
{
  LCD_DC_high;          //输入数据
  LCD_Pos(x,y);
  for(uint8 j=x;j<x+x_size;j++)
    LCD_write(0x00);
  LCD_Pos(x,y+1);
  for(uint8 j=x;j<x+x_size;j++)
    LCD_write(0x00);
}


