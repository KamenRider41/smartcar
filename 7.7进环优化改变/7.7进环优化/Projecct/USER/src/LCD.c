/*******************LCD_Round 画圆形********************/
void LCD_Round(uint8 x,uint8 y,uint8 r)
{
  uint8 i,aat=0x01;
  uint8 round_dot[128][2],dot[128],m;
  for(i=x-r;i<=x+r;i++)
  {
    m=(uint8)sqrt(r*r-(x-i)*(x-i));
    round_dot[i][0]=y+m;
    round_dot[i][1]=y-m;
  } 
  for(uint8 zt=(y-r)/8; zt<=(y+r)/8; zt++)
  {
    LCD_Pos(x-r,zt);
    
    for(uint8 wt=x-r;wt<=x+r;wt++)
    {
      uint8 mark=0;
      if(round_dot[wt][0]/8 == zt)  
      {
        dot[wt]= aat <<= round_dot[wt][0]%8;
        mark=1;aat=0x01; 
      }
      if(round_dot[wt][1]/8 == zt)  
      {
        if(mark!=1)
        {
          dot[wt]= aat <<= round_dot[wt][1]%8;
          aat=0x01;
        }
        else
        {
          uint8 bbt=0x01;
          bbt <<= round_dot[wt][1]%8;
          dot[wt]=dot[wt]|bbt;
          mark=0;
        }
      }
      if(round_dot[wt][0]/8 !=zt & round_dot[wt][1]/8 !=zt)
        dot[wt]=0x00;
      LCD_write(dot[wt]);      
    }    
  }
}

/*******************汉字字符混合********************/
void LCD_mixture(uint8 x, uint8 y, uint8 ch[])
{
  uint8 i=0,j=0,m=0,n=0,mark=0;
  while(ch[i] !='\0')
  {
    while(F16x16_Idx[j] != '\0' & mark==0)
    {
      if(F16x16_Idx[j] == ch[i] & F16x16_Idx[j+1] == ch[i+1])
        mark=1;
      else
        j=j+2;    
    }
    if(mark==1)            //汉字
    {
      if(x>112) {x=0;y=y+2;}
      LCD_Pos(x,y);
      for(m=0;m<16;m++)
        LCD_write(F16x16[j][m]);
      LCD_Pos(x,y+1);
      for(m=0;m<16;m++)
        LCD_write(F16x16[j+1][m]);
      mark=0;i=i+2;x=x+16;j=0;      
    }
    else            //字符
    {
      j=0;if(x>120){x=0;y=y+2;} 
      n=ch[i]-32;
      LCD_Pos(x,y);
      for(m=0;m<8;m++)
        LCD_write(Font8x16[n][m]);
      LCD_Pos(x,y+1);
      for(m=8;m<16;m++)
        LCD_write(Font8x16[n][m]);
      i=i+1;x=x+8;mark=0;         
    } 
  }
}

/*******************延时函数********************/
void LCD_Delay_Ms(uint16 ms)
{                         
  uint16 a;
  while(ms)
  {
    a=13350;
    while(a--);
    ms--;
  }
  return;
}

/*******************LCD_power x的y次方********************/
uint32 LCD_power(uint8 x,uint8 y)
{
  uint32 sum=1;
  while(y--)
    sum=sum*x;
  return sum;
}

